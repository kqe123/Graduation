package com.example.graduation;

public class Alcohol {
    private int bitterness;
    private String carbonated;
    private String category;
    private String content;
    private float degree;
    private String image;
    private String name;
    private int price;
    private int sweetness;
    private String taste;


    public int getBitterness() { return bitterness; }

    public void setBitterness(int bitterness) { this.bitterness = bitterness; }


    public String getCarbonated() { return carbonated; }

    public void setCarbonated(String carbonated) { this.carbonated = carbonated; }


    public String getCategory() { return category; }

    public void setCategory(String category) { this.category = category; }


    public String getContent() { return content; }

    public void setContent(String content) { this.content = content; }


    public float getDegree() { return degree; }

    public void setDegree(float degree) { this.degree = degree; }


    public String getImage() { return image; }

    public void setImage(String image) { this.image = image; }


    public String getName() { return name; }

    public void setName(String name) { this.name = name; }


    public int getPrice() { return price; }

    public void setPrice(int price) { this.price = price; }

    public int getSweetness() { return sweetness; }

    public void setSweetness(int sweetness) { this.sweetness = sweetness; }

    public String getTaste() { return taste; }

    public void setTaste(String taste) { this.taste = taste; }
}